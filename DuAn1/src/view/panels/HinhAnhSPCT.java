    private void LoadData(){
        DefaultTableModel tb = (DefaultTableModel) table1.getModel();
        tb.setRowCount(0);
        List<HinhAnh> ha =haService.getAll();
        int STT =1;
        for(HinhAnh x:ha){
            boolean trangThai=true;
            if(x.getTrang_thai() ==0){
                trangThai=true;
            }else{
                 trangThai=false;
            }
            tb.addRow(new Object[]{
                STT,
                x.getMa(),
                x.getDuong_dan(),
                trangThai
            });
        }
    }
    
