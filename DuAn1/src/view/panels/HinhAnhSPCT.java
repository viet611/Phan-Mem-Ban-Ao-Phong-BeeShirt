    public ImageIcon ResizeImage(String ImagePath){
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image image = MyImage.getImage();
        Image newImage = image.getScaledInstance(lblAnh.getWidth(), lblAnh.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon img = new ImageIcon(newImage);
        return img;
    }
    private HinhAnh getData(){
        HinhAnh ha= new HinhAnh();
        String prefix = "SP";
        Random random = new Random();
        int randomNumber = random.nextInt(100) + 1; // Generate a random number between 1 and 100
        String maSP =prefix + String.format("%02d", randomNumber);
        ha.setMa(maSP);
        String duongDan = duongDanAnh;
        ha.setDuong_dan(duongDan);
        int trangThai = 0;
        ha.setTrang_thai(trangThai);
        return ha;
    }
